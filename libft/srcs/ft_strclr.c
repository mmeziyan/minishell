void	ft_strclr(char *s)
{
	int i;

	if (s)
	{
		i = 0;
		while (s[i] || s[i - 1] || s[1 + i])
		{
			s[i] = '\0';
			s[i - 1] = '\0';
			i++;
		}
	}
}
