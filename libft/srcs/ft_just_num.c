#include "libft.h"

char	*ft_just_num(char *str)
{
	int		n;

	n = -1;
	if (str[0] == '-' || str[0] == '+')
		n++;
	while (str[++n])
	{
		if (!ft_isdigit(str[n]))
			return (str + n);
	}
	return (NULL);
}
